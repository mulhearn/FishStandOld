    public InvertShading(int nx, int ny){
        this.ny = ny;
        this.nx = nx;
    }

    double getfactor(int ix, int iy) {
        ix = ix * bx / nx;
        iy = iy * by / ny;
        if (ix >= ((int) bx / 2)) {
            ix = bx - ix - 1;
        }
        if (iy >= ((int) by / 2)) {
            iy = by - iy - 1;
        }
        return lut[bx * iy / 2 + ix];
    }
